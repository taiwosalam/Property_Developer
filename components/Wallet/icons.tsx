export const getTransactionIcon = (
  source: string,
  // type: "credit" | "debit" | "DVA" | "transfer_in" | "transfer_out"
  type:
    | "withdrawal"
    | "sponsor_listing"
    | "transfer_out"
    | "transfer_in"
    | "funding"
    | "debit"
) => {
  switch (type) {
    case "funding":
      return <WalletIncomingIcon />;
    case "withdrawal":
      return <WithrawalIcon />;
    case "transfer_out":
      return <ArrowOutgoingIcon />;
    case "transfer_in":
      return <ArrowIncomingIcon />;
    case "funding":
      return <TopUpIcon />;
    default:
      return type === "debit" ? <ArrowOutgoingIcon /> : <ArrowOutgoingIcon />;
  }
};
